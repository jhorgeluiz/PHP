<?php 


// A operação ternária é uma forma concisa de escrever 
// uma estrutura condicional if/else em uma única linha. 
// É muito usada para atribuições rápidas baseadas em uma condição.



$opcao = "verdadeiro";

$str = ($opcao) ? "verdadeiro" : "false";
echo $str;





// Dica
// A operação ternária é ótima para simplificar o código, 
// mas não exagere: se a lógica for muito complexa, 
// é melhor usar if/else para manter a clareza.
?>
